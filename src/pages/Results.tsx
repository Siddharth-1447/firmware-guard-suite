import { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { Navigation } from '@/components/Navigation';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from '@/components/ui/card';
import { Download, FileText, Shield, AlertTriangle, CheckCircle, XCircle } from 'lucide-react';
import { jsPDF } from 'jspdf';
import { toast } from 'sonner';
import { CircularProgress } from '@/components/CircularProgress';

export default function Results() {
  const [analysisData, setAnalysisData] = useState<any>(null);
  const navigate = useNavigate();

  useEffect(() => {
    const stored = sessionStorage.getItem('firmwareAnalysis');
    if (stored) {
      setAnalysisData(JSON.parse(stored));
    }
  }, []);

  const getRiskColor = (risk: string) => {
    switch (risk.toLowerCase()) {
      case 'low':
        return 'text-green-500';
      case 'medium':
        return 'text-yellow-500';
      case 'high':
      case 'critical':
        return 'text-red-500';
      default:
        return 'text-muted-foreground';
    }
  };

  const getRiskIcon = (risk: string) => {
    switch (risk.toLowerCase()) {
      case 'low':
        return <CheckCircle className="w-5 h-5 text-green-500" />;
      case 'medium':
        return <AlertTriangle className="w-5 h-5 text-yellow-500" />;
      case 'high':
      case 'critical':
        return <XCircle className="w-5 h-5 text-red-500" />;
      default:
        return null;
    }
  };

  const downloadPDF = () => {
    if (!analysisData) return;

    const doc = new jsPDF();
    
    // Logo and Title (Dark Blue - RGB: 0, 51, 102)
    doc.setFontSize(24);
    doc.setTextColor(0, 51, 102);
    doc.setFont('helvetica', 'bold');
    const title = 'CryptoFinder';
    const titleWidth = doc.getTextWidth(title);
    doc.text(title, (210 - titleWidth) / 2, 20);
    
    // Subtitle
    doc.setFontSize(12);
    doc.setTextColor(0, 0, 0);
    doc.setFont('helvetica', 'normal');
    const subtitle = 'Firmware Security Analysis Report';
    const subtitleWidth = doc.getTextWidth(subtitle);
    doc.text(subtitle, (210 - subtitleWidth) / 2, 28);
    
    // Divider line
    doc.setDrawColor(0, 0, 0);
    doc.line(20, 32, 190, 32);
    
    // File information section
    doc.setFontSize(11);
    doc.setFont('helvetica', 'bold');
    doc.text('File Information:', 20, 42);
    doc.setFont('helvetica', 'normal');
    doc.text(`File Name: ${analysisData.fileName}`, 20, 49);
    doc.text(`File Size: ${analysisData.fileSize}`, 20, 56);
    doc.text(`Analysis Date: ${analysisData.uploadDate} ${analysisData.uploadTime || ''}`, 20, 63);
    
    // Overall Safety Score
    doc.setFont('helvetica', 'bold');
    doc.text('Overall Safety Score:', 20, 75);
    doc.setFontSize(28);
    const scoreColor: [number, number, number] = analysisData.safetyPercentage >= 80 ? [34, 197, 94] : 
                       analysisData.safetyPercentage >= 50 ? [234, 179, 8] : [239, 68, 68];
    doc.setTextColor(scoreColor[0], scoreColor[1], scoreColor[2]);
    doc.text(`${analysisData.safetyPercentage}%`, 20, 88);
    
    // Algorithm Analysis Table
    doc.setFontSize(11);
    doc.setTextColor(0, 0, 0);
    doc.setFont('helvetica', 'bold');
    doc.text('Detected Cryptographic Algorithms:', 20, 100);
    
    // Table headers
    doc.setFillColor(240, 240, 240);
    doc.rect(20, 105, 170, 8, 'F');
    doc.setFont('helvetica', 'bold');
    doc.text('Algorithm', 25, 110);
    doc.text('Strength', 85, 110);
    doc.text('Risk Level', 130, 110);
    doc.text('Score', 165, 110);
    
    // Table rows
    let tableY = 118;
    doc.setFont('helvetica', 'normal');
    analysisData.algorithms.forEach((algo: any, index: number) => {
      // Alternating row colors
      if (index % 2 === 0) {
        doc.setFillColor(250, 250, 250);
        doc.rect(20, tableY - 5, 170, 8, 'F');
      }
      
      // Algorithm name
      doc.setTextColor(0, 0, 0);
      doc.text(algo.name, 25, tableY);
      
      // Strength
      doc.text(algo.strength, 85, tableY);
      
      // Risk (bold black if weak)
      if (algo.risk === 'High' || algo.risk === 'Critical') {
        doc.setFont('helvetica', 'bold');
      }
      doc.text(algo.risk, 130, tableY);
      doc.setFont('helvetica', 'normal');
      
      // Individual score
      doc.text(`${algo.score || 0}%`, 165, tableY);
      
      tableY += 8;
    });
    
    // Footer note
    doc.setFontSize(9);
    doc.setTextColor(100, 100, 100);
    doc.text(analysisData.note, 20, tableY + 10);
    doc.text('Generated by CryptoFinder - Secure Firmware Analysis Tool', 20, tableY + 16);
    
    doc.save(`CryptoFinder_Report_${analysisData.fileName}.pdf`);
    toast.success('Report downloaded successfully!');
  };

  if (!analysisData) {
    return (
      <div className="min-h-screen bg-background">
        <Navigation />
        <main className="container mx-auto px-6 pt-24 pb-12">
          <div className="text-center">
            <FileText className="w-16 h-16 mx-auto mb-4 text-muted-foreground" />
            <h2 className="text-2xl font-bold mb-2">No Analysis Results</h2>
            <p className="text-muted-foreground mb-6">Upload a firmware file to see analysis results</p>
            <Button onClick={() => navigate('/dashboard')} className="bg-primary hover:bg-primary/90">
              Go to Dashboard
            </Button>
          </div>
        </main>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-background">
      <Navigation />
      
      <main className="container mx-auto px-6 pt-24 pb-12">
        <div className="max-w-4xl mx-auto animate-fade-in">
          <div className="mb-8">
            <h1 className="text-4xl font-bold mb-2 bg-gradient-to-r from-primary to-primary-glow bg-clip-text text-transparent">
              Analysis Results
            </h1>
            <p className="text-muted-foreground">Comprehensive firmware security assessment</p>
          </div>

          <div className="grid gap-6">
            {/* Summary Card with Circular Progress */}
            <Card className="bg-card/50 backdrop-blur-sm border-border shadow-glow">
              <CardHeader>
                <CardTitle className="flex items-center gap-2">
                  <Shield className="w-6 h-6 text-primary" />
                  Firmware Overview
                </CardTitle>
              </CardHeader>
              <CardContent className="space-y-6">
                <div className="grid md:grid-cols-3 gap-4">
                  <div>
                    <p className="text-sm text-muted-foreground">File Name</p>
                    <p className="font-semibold break-all">{analysisData.fileName}</p>
                  </div>
                  <div>
                    <p className="text-sm text-muted-foreground">File Size</p>
                    <p className="font-semibold">{analysisData.fileSize}</p>
                  </div>
                  <div>
                    <p className="text-sm text-muted-foreground">Upload Date</p>
                    <p className="font-semibold">{analysisData.uploadDate}</p>
                  </div>
                </div>
                
                <div className="pt-6 border-t border-border flex flex-col items-center">
                  <CircularProgress percentage={analysisData.safetyPercentage} />
                  <p className="text-sm text-muted-foreground mt-6 text-center">{analysisData.note}</p>
                </div>
              </CardContent>
            </Card>

            {/* Algorithms Table */}
            <Card className="bg-card/50 backdrop-blur-sm border-border">
              <CardHeader>
                <CardTitle>Detected Cryptographic Algorithms</CardTitle>
                <CardDescription>Complete breakdown of encryption and hashing algorithms found</CardDescription>
              </CardHeader>
              <CardContent>
                <div className="overflow-x-auto">
                  <table className="w-full">
                    <thead>
                      <tr className="border-b border-border">
                        <th className="text-left py-3 px-4 font-semibold">Algorithm</th>
                        <th className="text-left py-3 px-4 font-semibold">Strength</th>
                        <th className="text-left py-3 px-4 font-semibold">Risk Level</th>
                        <th className="text-left py-3 px-4 font-semibold">Status</th>
                      </tr>
                    </thead>
                    <tbody>
                      {analysisData.algorithms.map((algo: any, index: number) => (
                        <tr key={index} className="border-b border-border hover:bg-secondary/50 transition-colors animate-fade-in" style={{ animationDelay: `${index * 50}ms` }}>
                          <td className="py-4 px-4 font-medium">{algo.name}</td>
                          <td className="py-4 px-4">{algo.strength}</td>
                          <td className={`py-4 px-4 font-semibold ${getRiskColor(algo.risk)}`}>
                            {algo.risk}
                          </td>
                          <td className="py-4 px-4 flex items-center gap-2">
                            {getRiskIcon(algo.risk)}
                            <span className="text-sm text-muted-foreground">{algo.score}%</span>
                          </td>
                        </tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              </CardContent>
            </Card>

            {/* Actions */}
            <div className="flex flex-wrap gap-4">
              <Button onClick={downloadPDF} className="bg-primary hover:bg-primary/90 shadow-glow">
                <Download className="w-4 h-4 mr-2" />
                Download Report as PDF
              </Button>
              <Button onClick={() => navigate('/dashboard')} variant="outline" className="border-border hover:bg-secondary">
                Analyze Another File
              </Button>
            </div>
          </div>
        </div>
      </main>
    </div>
  );
}
